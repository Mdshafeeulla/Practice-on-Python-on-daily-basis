# How can we append inside the list
Name = ["Vinod", "Stephen", "George", "Mohammed"]
Name.append("Shafeeulla")
print(Name)

#How can we extend the list
Names = ["Shahid"]
Name.extend(Names)
print(Name)

#Remove item from the list
Name.remove("Vinod")
print(Name)

#Pop item from the list
Name.pop()
print(Name)